public class Calculator {

    int add(int a, int b){ 
        int resultado = a + b;    
        return resultado; 
    }
    int sub(int a, int b){ 
        int resultado = a - b;
        return resultado; 
    }
    int mul(int a, int b){ 
        int resultado = a * b;
        return resultado; 
    }
    int div(int a, int b){ 
        int resultado = a / b;
        return resultado; 
    }
    int mod(int a, int b){ 
        int resultado = a % b;
        return resultado; 
    } 
}
